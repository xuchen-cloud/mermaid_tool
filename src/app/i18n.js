export function normalizeUiLanguage(value) {
  return value === "zh-CN" ? "zh-CN" : "en";
}

export function loadUiLanguage() {
  const saved = window.localStorage.getItem(uiLanguageStorageKey);
  if (saved === "en" || saved === "zh-CN") {
    return saved;
  }

  return /^zh\b/i.test(navigator.language ?? "") ? "zh-CN" : "en";
}

export function createTranslator(initialLanguage) {
  let currentLanguage = normalizeUiLanguage(initialLanguage ?? loadUiLanguage());

  return {
    get language() {
      return currentLanguage;
    },
    setLanguage(nextLanguage) {
      currentLanguage = normalizeUiLanguage(nextLanguage);
    },
    t(key, vars = {}) {
      const template = uiMessages[currentLanguage]?.[key] ?? uiMessages.en[key] ?? key;
      return template.replace(/\{(\w+)\}/g, (_, name) => String(vars[name] ?? ""));
    }
  };
}
